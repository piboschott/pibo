export function seedTelemetryValidationFixtures(telemetry, options = {}) {
	const ids = { ...telemetryValidationFixtureIds, ...options.ids };
	const clock = { ...telemetryValidationFixtureClock, ...options.clock };
	const provider = options.provider ?? "openai";
	const model = options.model ?? "gpt-fixture";
	const api = options.api ?? "openai-responses";

	seedCompletedAssistantTelemetryFixture(telemetry, { ids, clock, provider, model, api });
	const stuck = seedStuckToolCallTelemetryFixture(telemetry, options.stuckOptions ?? {});
	seedMalformedProviderTelemetryFixture(telemetry, { ids, clock, provider, model, api });
	seedUnknownProviderTelemetryFixture(telemetry, { ids, clock, provider, model, api });
	seedStaleToolExecutionTelemetryFixture(telemetry, { ids, clock, provider, model, api });

	return { ids, clock, stuck, secrets: telemetryValidationFixtureSecrets };
}

